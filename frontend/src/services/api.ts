import axios from 'axios';

const API_URL = import.meta.env.VITE_API_URL || 'http://localhost:5000/api';

const api = axios.create({
  baseURL: API_URL,
  headers: {
    'Content-Type': 'application/json',
  },
});

// Request interceptor for adding auth token
api.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => Promise.reject(error)
);

// Response interceptor for handling errors
api.interceptors.response.use(
  (response) => response,
  (error) => {
    // Handle session expiration - but not for login or profile update errors
    if (error.response?.status === 401) {
      const isLoginAttempt = error.config?.url?.includes('/login');
      const isProfileUpdate = error.config?.url?.includes('/profile') && error.config?.method === 'put';
      
      // Don't auto-logout if it's a login attempt or profile update with wrong password
      if (!isLoginAttempt && !isProfileUpdate) {
        localStorage.removeItem('token');
        localStorage.removeItem('user');
        window.location.href = '/login';
      }
    }
    return Promise.reject(error);
  }
);

export default api;